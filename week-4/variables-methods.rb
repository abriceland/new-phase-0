How do you define a local variable?

It is a way to store information.  It can be assigned any variety of letters or numbers.  It begins in Ruby with a lowercase letter.


How do you define a method?

It is an action that affects a variable.  Methods are called on objects.  


What is the difference between a local variable and a method?

The variable is like a noun and the method is the verb of Ruby syntax.  A method acts upon upon a variable or another object.  In Ruby, the variable is stated before the method with a period (.) in between.  


How do you run a ruby program from the command line?

You type ruby file_name.rb  and it will run the file.  


How do you run an RSpec file from the command line?

You type rspec rspec_file_name.rb  .  If it is correctly linked with the code using require_relative "file_name", it can check the code.


